"""
match_pbac_nice.py
━━━━━━━━━━━━━━━━━━
Cross-references PBAC Public Summary Documents with NICE Technology Appraisals
to produce a side-by-side comparison dataset: same drug, two countries, two dates.

KEY OUTPUTS:
  pbac_nice_matched.csv     — matched pairs with:
                              drug, pbac_date, nice_date, gap_days, gap_months,
                              who_moved_first, pbac_outcome, nice_recommendation
  pbac_nice_unmatched.csv   — PSDs with no NICE TA match (AUS-only approvals)
  nice_pbac_unmatched.csv   — NICE TAs with no PBAC match (UK-only approvals)

ANALYSIS QUESTIONS THIS ENABLES:
  - How many months does Australia lag behind the UK for the same drug?
  - Are PBAC and NICE concordant? (both recommend / both reject / split)
  - Which therapy areas have the biggest access gaps?
  - For which drugs did Australia actually move first?

REQUIREMENTS:
  - nice_metadata.csv (generated by download_nice_missing.py OR manually created)
  - The PSD_database folder with PBAC PSDs (*.pdf files named {drug}-psd-*.pdf)

  pip install thefuzz python-Levenshtein  (for fuzzy matching — optional but better)

USAGE:
  python match_pbac_nice.py
  python match_pbac_nice.py --psd-dir /path/to/PSDs --nice-meta nice_metadata.csv

MATCHING APPROACH:
  1. Exact match: normalised drug name (lowercase, remove spaces/hyphens)
  2. Prefix match: PBAC name starts with NICE name or vice versa (handles brand/generic)
  3. Fuzzy match (if thefuzz installed): Levenshtein similarity ≥ 85%
  4. Manual overrides: edit MANUAL_ALIASES below for known mismatches
"""

