def classify(drug: str) -> str:
    d = drug.lower()
    for area, kws in THERAPY_KEYWORDS.items():
        for kw in kws:
            if kw in d:
                return area
    return "Other"


# ── main ──────────────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(description="Match PBAC PSDs with NICE TAs")
    parser.add_argument("--psd-dir",   default=str(PSD_DIR),   help="Folder containing PBAC PSDs")
    parser.add_argument("--nice-meta", default=str(META_PATH),  help="Path to nice_metadata.csv")
    parser.add_argument("--out-dir",   default=str(OUT_DIR),    help="Output folder for CSVs")
    args = parser.parse_args()

    psd_dir   = Path(args.psd_dir)
    meta_path = Path(args.nice_meta)
    out_dir   = Path(args.out_dir)

    log.info("=" * 65)
    log.info("PBAC ↔ NICE Matcher")
    log.info(f"PSDs      : {psd_dir}")
    log.info(f"NICE meta : {meta_path}")
    log.info(f"Output    : {out_dir}")
    if not FUZZY_AVAILABLE:
        log.info("  (Fuzzy matching disabled — run: pip install thefuzz python-Levenshtein)")
    log.info("=" * 65)

    # ── Load data ──────────────────────────────────────────────────────────
    pbac_records = parse_pbac_psds(psd_dir)
    nice_records = load_nice_metadata(meta_path)

    log.info(f"PBAC PSDs loaded : {len(pbac_records)}")
    log.info(f"NICE TAs loaded  : {len(nice_records)}")

    if not nice_records:
        log.error("No NICE metadata — run download_nice_missing.py first.")
        sys.exit(1)

    # ── Match ──────────────────────────────────────────────────────────────
    matched      = []
    pbac_only    = []
    matched_nice = set()   # ta_numbers that got matched

    for pr in pbac_records:
        nice_hits = match_drug(pr["drug"], nice_records)

        if not nice_hits:
            pbac_only.append(pr)
            continue

        # For drugs with multiple NICE TAs (e.g. nivolumab used in many cancers),
        # record each pair separately.
        for nr in nice_hits:
            gap_days, gap_months, who_first = date_gap(pr["pbac_date"], nr.get("nice_date"))

            matched.append({
                "drug":               pr["drug"],
                "therapy_area":       classify(pr["drug"]),
                "pbac_filename":      pr["filename"],
                "pbac_year":          pr["year"],
                "pbac_month":         pr["month"] or "",
                "pbac_date":          pr["pbac_date"].isoformat() if pr["pbac_date"] else "",
                "nice_ta_number":     nr["ta_number"],
                "nice_title":         nr.get("title", ""),
                "nice_published_date":nr.get("published_date", ""),
                "nice_date":          nr["nice_date"].isoformat() if nr.get("nice_date") else "",
                "nice_recommendation":nr.get("recommendation", ""),
                "gap_days":           gap_days if gap_days is not None else "",
                "gap_months":         gap_months if gap_months is not None else "",
                "who_moved_first":    who_first,
                "nice_guidance_url":  nr.get("guidance_url", ""),
            })
            matched_nice.add(nr["ta_number"])

    # NICE TAs with no PBAC match
    nice_only = [nr for nr in nice_records if nr["ta_number"] not in matched_nice]

    log.info(f"\nResults:")
    log.info(f"  Matched pairs  : {len(matched)}")
    log.info(f"  PBAC-only PSDs : {len(pbac_only)}  (no matching NICE TA)")
    log.info(f"  NICE-only TAs  : {len(nice_only)}   (no matching PBAC PSD)")

    # ── Summary stats ──────────────────────────────────────────────────────
    gaps = [r["gap_months"] for r in matched if isinstance(r["gap_months"], (int, float))]
    if gaps:
        avg_gap = round(sum(gaps) / len(gaps), 1)
        median_gap = sorted(gaps)[len(gaps) // 2]
        aus_lagged  = sum(1 for g in gaps if g > 1)
        aus_led     = sum(1 for g in gaps if g < -1)
        concurrent  = len(gaps) - aus_lagged - aus_led
        log.info(f"\nAccess gap analysis (positive = UK approved first):")
        log.info(f"  Average gap   : {avg_gap} months")
        log.info(f"  Median gap    : {median_gap} months")
        log.info(f"  UK first      : {aus_lagged} drugs")
        log.info(f"  AUS first     : {aus_led} drugs")
        log.info(f"  Concurrent    : {concurrent} drugs")

    # ── Save outputs ───────────────────────────────────────────────────────
    matched_fields = [
        "drug","therapy_area","pbac_date","nice_date","gap_days","gap_months",
        "who_moved_first","nice_recommendation","pbac_filename","nice_ta_number",
        "nice_title","nice_published_date","pbac_year","pbac_month","nice_guidance_url",
    ]

    matched_path = out_dir / "pbac_nice_matched.csv"
    with open(matched_path, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=matched_fields, extrasaction="ignore")
        w.writeheader()
        # Sort by drug name for readability
        w.writerows(sorted(matched, key=lambda r: r["drug"]))
    log.info(f"\nSaved: {matched_path}  ({len(matched)} rows)")

    pbac_only_path = out_dir / "pbac_nice_unmatched.csv"
    with open(pbac_only_path, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=["drug","year","month","pbac_date","filename"],
                           extrasaction="ignore")
        w.writeheader()
        w.writerows(pbac_only)
    log.info(f"Saved: {pbac_only_path}  ({len(pbac_only)} rows)")

    nice_only_path = out_dir / "nice_pbac_unmatched.csv"
    with open(nice_only_path, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=["ta_number","drug_name","title","published_date","recommendation","guidance_url"],
                           extrasaction="ignore")
        w.writeheader()
        w.writerows(sorted(nice_only, key=lambda r: r.get("drug_name","")))
    log.info(f"Saved: {nice_only_path}  ({len(nice_only)} rows)")

    log.info("")
    log.info("=" * 65)
    log.info("DONE — open pbac_nice_matched.csv in Excel to explore!")
    log.info("=" * 65)


if __name__ == "__main__":
    main()
