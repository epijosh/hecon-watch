# ── date parsing ──────────────────────────────────────────────────────────────
def parse_nice_date(s: str) -> "date | None":
    """Parse NICE published_date strings like '29 March 2017', 'March 2017', '2017'."""
    if not s:
        return None
    s = s.strip()

    # "29 March 2017"
    m = re.match(r"(\d{1,2})\s+(\w+)\s+(\d{4})", s)
    if m:
        mo = MONTH_MAP.get(m.group(2).lower())
        if mo:
            try:
                return date(int(m.group(3)), mo, int(m.group(1)))
            except ValueError:
                pass

    # "March 2017"
    m = re.match(r"(\w+)\s+(\d{4})", s)
    if m:
        mo = MONTH_MAP.get(m.group(1).lower())
        if mo:
            return date(int(m.group(2)), mo, 15)  # mid-month approximation

    # "2017"
    m = re.match(r"(\d{4})$", s)
    if m:
        return date(int(m.group(1)), 7, 1)  # mid-year approximation

    return None


def parse_pbac_date(year: int, month: "int | None") -> "date | None":
    """Convert PBAC year + month (from filename) to a date."""
    if year is None:
        return None
    if month is None:
        return date(year, 7, 1)   # mid-year approximation
    # PBAC meeting months are 3 (March), 7 (July), 11 (November)
    # PSDs are published ~4 months after the meeting, so the listing date
    # is approximately meeting month + 4. We record the meeting date.
    return date(year, month, 1)


# ── PBAC PSD parsing ──────────────────────────────────────────────────────────
def parse_pbac_psds(psd_dir: Path) -> list[dict]:
    """
    Parse all {drug}-psd-{month}-{year}.pdf files in psd_dir.
    Returns list of dicts: filename, drug, year, month, pbac_date.
    """
    records = []
    for f in sorted(psd_dir.glob("*.pdf")):
        if "psd" not in f.name.lower():
            continue

        s = f.name.lower().replace("%20", "-").replace(" ", "-")
        s = re.sub(r"\.pdf$", "", s)

        # Extract drug name (everything before -psd)
        m = re.match(r"^(.+?)-psd", s)
        drug = m.group(1).strip("-") if m else s

        # Extract date
        year, month = None, None
        m2 = re.search(r"-psd[^-]*-([a-z]+)-(\d{4})", s)
        if m2:
            mo = MONTH_MAP.get(m2.group(1))
            yr = int(m2.group(2))
            if mo and 2000 <= yr <= 2030:
                year, month = yr, mo
        if year is None:
            m3 = re.search(r"-psd[^-]*-(\d{1,2})-(\d{4})", s)
            if m3:
                mo, yr = int(m3.group(1)), int(m3.group(2))
                if 1 <= mo <= 12 and 2000 <= yr <= 2030:
                    year, month = yr, mo
        if year is None:
            m4 = re.search(r"-psd[^-]*-(\d{4})", s)
            if m4:
                yr = int(m4.group(1))
                if 2000 <= yr <= 2030:
                    year = yr
        if year is None:
            m5 = re.search(r"(\d{4})-(\d{2})(?:-final)?$", s)
            if m5:
                yr, mo = int(m5.group(1)), int(m5.group(2))
                if 2000 <= yr <= 2030 and 1 <= mo <= 12:
                    year, month = yr, mo

        if year is None:
            continue

        records.append({
            "filename":   f.name,
            "drug":       drug,
            "year":       year,
            "month":      month,
            "pbac_date":  parse_pbac_date(year, month),
        })

    return records


def load_nice_metadata(meta_path: Path) -> list[dict]:
    """Load nice_metadata.csv and parse dates."""
    if not meta_path.exists():
        log.error(f"nice_metadata.csv not found at: {meta_path}")
        log.error("Run download_nice_missing.py first to generate it.")
        return []

    records = []
    with open(meta_path, encoding="utf-8", newline="") as f:
        for row in csv.DictReader(f):
            row["nice_date"] = parse_nice_date(row.get("published_date", ""))
            row["ta_number"] = int(row["ta_number"]) if row.get("ta_number") else 0
            records.append(row)
    return records


# ── drug name normalisation ───────────────────────────────────────────────────
def normalise(name: str) -> str:
    """Lowercase, remove hyphens/spaces/punctuation, apply aliases."""
    n = name.lower().strip()
    # Apply manual aliases first
    for alias_from, alias_to in MANUAL_ALIASES.items():
        if alias_from in n:
            n = alias_to
            break
    # Remove hyphens and whitespace
    n = re.sub(r"[\s\-]+", "", n)
    # Remove common suffixes that differ between countries
    n = re.sub(r"(alfa|beta|gamma|delta|hydro|sodium|chloride|acetate|phosphate|sulphate|sulfate)$", "", n)
    return n


# ── matching ──────────────────────────────────────────────────────────────────
def match_drug(pbac_drug: str, nice_records: list[dict]) -> "list[dict]":
    """
    Find all NICE records that match a PBAC drug name.
    Returns list of matching NICE records (there may be multiple TAs for one drug).
    """
    p_norm = normalise(pbac_drug)
    matches = []

    for nr in nice_records:
        n_norm = normalise(nr.get("drug_name", ""))
        if not n_norm:
            continue

        # 1. Exact match
        if p_norm == n_norm:
            matches.append(nr)
            continue

        # 2. Prefix match (one is a prefix of the other)
        shorter, longer = (p_norm, n_norm) if len(p_norm) <= len(n_norm) else (n_norm, p_norm)
        if len(shorter) >= 4 and longer.startswith(shorter):
            matches.append(nr)
            continue

        # 3. Substring match (for compound drug names, e.g. sacubitril-valsartan)
        if len(p_norm) >= 6 and p_norm in n_norm:
            matches.append(nr)
            continue
        if len(n_norm) >= 6 and n_norm in p_norm:
            matches.append(nr)
            continue

        # 4. Fuzzy match
        if FUZZY_AVAILABLE and len(p_norm) >= 5 and len(n_norm) >= 5:
            score = fuzz.ratio(p_norm, n_norm)
            if score >= FUZZY_THRESHOLD:
                matches.append(nr)

    return matches


# ── gap calculation ────────────────────────────────────────────────────────────
def date_gap(pbac_date: "date | None", nice_date: "date | None") -> "tuple[int | None, float | None, str]":
    """
    Returns (gap_days, gap_months, who_moved_first).
    Positive gap means NICE was first (Australia lagged).
    Negative gap means PBAC was first (Australia led).
    """
    if not pbac_date or not nice_date:
        return None, None, "unknown"

    delta = (pbac_date - nice_date).days
    months = round(delta / 30.44, 1)

    if delta > 30:
        who_first = "NICE (UK first)"
    elif delta < -30:
        who_first = "PBAC (AUS first)"
    else:
        who_first = "Concurrent (±1mo)"

    return delta, months, who_first


